 WHERE 구분코드 != '휴대폰' ;
 
--문제2 ) 나이가 50살 미만이 아닌 직원의 직원ID , 이름 , 성별 , 나이 를 출력해주세요. 
SELECT 직원ID
     , 이름
     , 성별
     , 나이
  FROM 직원
 WHERE 나이 >= 50 ;
 
--문제1 ) 직원 테이블에서 나이가 NULL 이 아닌 정보만 모두 출력해주세요.
