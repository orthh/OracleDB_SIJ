--1. 직원 테이블에서 입사일시 컬럼을 이용해 입사한 년도(YYYY)만 추출해주세요.
SELECT 직원ID
     , 입사일시
     , TO_CHAR(입사일시, 'YYYY') AS 입사연도
  FROM 직원 ;

--1. 2023년이 되었는데 직원 테이블에 나이 정보가 수정되지 않았습니다.
--현재 나이에서 +1 을 하여 직원 정보를 출력해주세요.
SELECT 직원ID
     , 나이
     , 나이 + 1 AS 올해나이
  FROM 직원 ;
  
--1. 직원 테이블에서 정보를 출력하되 나이가 정해지지 않은 대상은 일단 20 으로 출력되도록 해주세요. 
--(NVL 활용)
SELECT 직원ID
     , 패스워드
     , NVL(나이, 20) AS 나이
  FROM 직원 ;
  
--2. 직원 테이블에서 성별이 ‘남’ 이면 ‘남성입니다‘ 를 출력, 그렇지않으면 ‘여성입니다’ 를 출력해주세요.
--(DECODE 활용)
SELECT 직원ID
     , 성별
     , 연봉
     , DECODE(성별, '남', '남성입니다', '여성입니다' ) AS 남녀확인
  FROM 직원 ;
  
--(1) 직원 중에 연봉이 9000 보다 많은 직원들의 직원ID , 이름 , 나이 , 입사일시를 출력해주세요.
SELECT 직원ID
     , 이름
     , 나이
     , 입사일시
  FROM 직원
 WHERE 연봉 > 9000 ;
 
--(2) 직원 중에 이름이 “이현정“ 인 직원의 정보를 모두 출력해주세요.
SELECT *
  FROM 직원
 WHERE 이름 = '이현정' ;
 
--(3) 직원연락처 테이블에서 연락처가 ‘010-1231-1234’ 인 직원의 정보를 모두 출력해주세요.
SELECT *
  FROM 직원연락처
 WHERE 연락처 = '010-1231-1234' ;
 
--(4) 부서 테이블에서 근무지가 서울인 부서명들을 출력해주세요
SELECT *
  FROM 부서
 WHERE 근무지 = '서울' ;
 
--(5) 직원주소 테이블에서 직원ID 가 ‘A0007’ 인 직원의 주소를 출력해주세요
SELECT 주소
  FROM 직원주소
 WHERE 직원ID = 'A0007' ;
 
 --문제1 ) 직원연락처 테이블에서 직원ID가 ‘A0001’ 인 직원의 집전화 연락처를 출력해주세요
SELECT 연락처
  FROM 직원연락처
 WHERE 직원ID = 'A0001'
   AND 구분코드 = '집전화' ;
   
--문제2 ) 소속된 부서의 ID가 ‘D003’인 여직원 중 나이가 30대 이상인 직원의 모든 정보를 출력해주세요.
SELECT *
  FROM 직원
 WHERE 부서ID = 'D003' AND 성별 = '여' AND 나이 >=30 ;
 
--문제3 ) 직원ID 가 ‘A0001’ 이거나 ‘A0005’ 이거나 ‘A0007’ 인 직원의 정보를 모두 출력해주세요.
SELECT *
  FROM 직원
 WHERE 직원ID IN ('A0001', 'A0005', 'A0007') ;
 
--문제1 ) 직원연락처 테이블에서 구분코드가 ‘휴대폰‘ 이 아닌 데이터의 모든 정보를 출력해주세요.
SELECT * 
